package ua.nau.edu.RecyclerViews.MapsActivity.Search;

public class MapsSearchDataModel {
    String name;
    int id;

    public MapsSearchDataModel(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

}
