package ua.nau.edu.RecyclerViews.MapsActivity;

public class MapsDataModel {
    String name;
    int id;

    public MapsDataModel(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

}
